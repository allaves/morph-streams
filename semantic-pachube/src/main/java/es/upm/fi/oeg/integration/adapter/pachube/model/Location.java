package es.upm.fi.oeg.integration.adapter.pachube.model;

public class Location 
{
	private double lat;
	private double lon;
	public void setLat(double lat) {
		this.lat = lat;
	}
	public double getLat() {
		return lat;
	}
	public void setLon(double lon) {
		this.lon = lon;
	}
	public double getLon() {
		return lon;
	}
}
